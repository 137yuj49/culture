package com.cp.ticketing;

public class Ticketing {
	private String ticketing_no;
	private String ticket_no;
	private int ticketing_count;
	
	public String getTicketing_no() {
		return ticketing_no;
	}
	public void setTicketing_no(String ticketing_no) {
		this.ticketing_no = ticketing_no;
	}
	public String getTicket_no() {
		return ticket_no;
	}
	public void setTicket_no(String ticket_no) {
		this.ticket_no = ticket_no;
	}
	public int getTicketing_count() {
		return ticketing_count;
	}
	public void setTicketing_count(int ticketing_count) {
		this.ticketing_count = ticketing_count;
	}
	
}
